import UIKit
import WebKit
import FloatingTabBarController

class SceneDelegate: UIResponder, UIWindowSceneDelegate {

    var window: UIWindow?


    func scene(_ scene: UIScene, willConnectTo session: UISceneSession, options connectionOptions: UIScene.ConnectionOptions) {
        if let windowScene = scene as? UIWindowScene {
            let window = UIWindow(windowScene: windowScene)
            
            var viewControllers: [UIViewController] = []
            
            let first = ViewController()
            let nc = UINavigationController(rootViewController: first)
            first.view.backgroundColor = .orange
            first.title = "First"
            first.tabBarItem.image = UIImage(named: "First")
            viewControllers.append(nc)
            
            let second = WebViewController()
            second.view.backgroundColor = .green
            second.title = "Second"
            second.tabBarItem.image = UIImage(named: "Second")
            viewControllers.append(second)
            
            let third = TableViewController()
            third.title = "Third"
            third.tabBarItem.image = UIImage(named: "Third")
            viewControllers.append(UINavigationController(rootViewController: third))
            
            let tbc = FloatingTabBarController()
            tbc.floatingTabBar = GrayTabBar(frame: CGRect.zero)
            tbc.viewControllers = viewControllers
            window.rootViewController = tbc
            self.window = window
            window.makeKeyAndVisible()            
        }
    }

